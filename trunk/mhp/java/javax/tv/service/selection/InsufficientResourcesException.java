
package javax.tv.service.selection;

/*

<code>InsufficientResourcesException</code> is generated when
 sufficient resources for an operation are not available.

*/
public class InsufficientResourcesException extends ServiceContextException {

/*
 
 Constructs an InsufficientResourcesException with no
 detail message. 
 */

public InsufficientResourcesException (){
   super();
}


/*
 
 Constructs an InsufficientResourcesException with a
 detail message. 
 Parameters:  s - The detail message. 
 
 */

public InsufficientResourcesException (java.lang.String s){
   super(s);
}



}

