
package javax.tv.service;

/*

The base class for exceptions related to service information.

*/
public class SIException extends java.lang.Exception {

/*
 
 Constructs an SIException with no detail message. 
 */

public SIException (){
}


/*
 
 Constructs an SIException with a detail message. 
 Parameters:  reason - The reason why this exception was thrown. 
 
 */

public SIException (java.lang.String reason){
}



}

